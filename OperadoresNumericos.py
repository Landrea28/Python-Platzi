#Operadores numericos
a = 10
b = 3
print("suma", a + b)
print("resta", a - b)
print("multiplicacion", a * b)
print("potencioacion", a ** b) #por ejemplo 10^3
print("division", a / b)
print("parte entera de la division", a // b)
print("modulo", a % b)
a += 2
print(a)
a -= -2
print(a)
